%%--------------------------------------------------------------------
%% @doc milliseconds since epoch (an int time stamp)
%% @spec now() -> int()
%% @end
%%--------------------------------------------------------------------
now() ->
    {Mega,Sec,Micro} = erlang:now(),
    (Mega*1000000+Sec)*1000000+Micro.

